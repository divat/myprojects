package com.cm.style.profile.model;

public class StyleDetails {

	private String clientName;
	private String publisherName;
	private String publicatioName;
	private String styleUID;
	private String createdOn;
	private String modifiedOn;
	
	public String getClientName() {
		return clientName;
	}
	public void setClientName(String clientName) {
		this.clientName = clientName;
	}
	public String getPublisherName() {
		return publisherName;
	}
	public void setPublisherName(String publisherName) {
		this.publisherName = publisherName;
	}
	public String getPublicatioName() {
		return publicatioName;
	}
	public void setPublicatioName(String publicatioName) {
		this.publicatioName = publicatioName;
	}
	public String getStyleUID() {
		return styleUID;
	}
	public void setStyleUID(String styleUID) {
		this.styleUID = styleUID;
	}
	public String getCreatedOn() {
		return createdOn;
	}
	public void setCreatedOn(String createdOn) {
		this.createdOn = createdOn;
	}
	public String getModifiedOn() {
		return modifiedOn;
	}
	public void setModifiedOn(String modifiedOn) {
		this.modifiedOn = modifiedOn;
	}	
}